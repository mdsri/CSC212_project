
public class Contact {

	String name;
	int Phnumber;
	String email;
	String address;
	String birthday;
	String notes;

	public Contact(String name, int Phnumber, String email, String address, String birthday, String notes) {
		this.name = name;
		this.Phnumber = Phnumber;
		this.email = email;
		this.address = address;
		this.birthday = birthday;
		this.notes = notes;
	}

}